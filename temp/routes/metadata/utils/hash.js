const {HMAC256HEX}      = require('../../../common/utils/crypto/hmac');

var _objectToHash = function( _metadata ) {
    let plainText = JSON.stringify( _metadata );
    return HMAC256HEX( plainText, undefined );
}

module.exports._objectToHash     = _objectToHash;